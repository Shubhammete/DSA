package Class.Concepts.JavaBasics;

public class Loops {
    public static void main(String[] args) {
        // if-else
        if(true){
            System.out.println("Its true");
        }else{
            System.out.println("haha");
        }

        // while loop
        int i = 1;
        while(i < 5){
            System.out.println(i);
            i++;
        }

        // For loop

        for(int j = 0;j<7;j++){
            System.out.println(j);
        }
    }
}
